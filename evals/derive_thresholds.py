"""Derive per-metric hard floors from committed baseline runs.

Reads every `evals/baselines/run-*.json`, pools per-entry scores per
metric, drops NaN (judge noise), computes `mean − 1σ` per metric using
population stdev, prints an audit table, and overwrites
`evals/thresholds.py`.

Refuses to run with fewer than 3 baseline files or fewer than 5
non-NaN observations for any metric — see plans/eval-framework.md.

Invocation:
    uv run python -m evals.derive_thresholds
"""

from __future__ import annotations

import json
import math
import statistics
import sys
from pathlib import Path

from evals.scoring import METRIC_NAMES

MIN_BASELINES = 3
MIN_OBSERVATIONS = 5

BASELINES_DIR = Path(__file__).parent / "baselines"
THRESHOLDS_FILE = Path(__file__).parent / "thresholds.py"


def _load_observations() -> tuple[list[Path], dict[str, list[float]]]:
    """Return (baseline_files, {metric: [non-NaN scores across all runs]})."""
    files = sorted(BASELINES_DIR.glob("run-*.json"))
    obs: dict[str, list[float]] = {name: [] for name in METRIC_NAMES}
    for path in files:
        run = json.loads(path.read_text())
        for entry in run.values():
            scores = entry.get("scores", {})
            for metric in METRIC_NAMES:
                value = scores.get(metric)
                if value is None:
                    continue
                if isinstance(value, float) and math.isnan(value):
                    continue
                obs[metric].append(float(value))
    return files, obs


def _derive(obs: dict[str, list[float]]) -> dict[str, tuple[int, float, float, float]]:
    """Return {metric: (n, mean, stdev, floor)}."""
    out: dict[str, tuple[int, float, float, float]] = {}
    for metric, values in obs.items():
        n = len(values)
        mean = statistics.fmean(values)
        stdev = statistics.pstdev(values)
        floor = max(0.0, mean - stdev)
        out[metric] = (n, mean, stdev, floor)
    return out


def _render_thresholds_file(floors: dict[str, float]) -> str:
    lines = [
        '"""Per-metric hard floors for the offline eval suite.',
        "",
        "Generated by `evals/derive_thresholds.py` from the baseline runs",
        "committed under `evals/baselines/`. Re-run after committing a new",
        "baseline to refresh.",
        '"""',
        "",
        "from __future__ import annotations",
        "",
        "THRESHOLDS: dict[str, float] = {",
    ]
    for metric in METRIC_NAMES:
        lines.append(f'    "{metric}": {floors[metric]:.4f},')
    lines.append("}")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    files, obs = _load_observations()
    if len(files) < MIN_BASELINES:
        print(
            f"need ≥{MIN_BASELINES} baseline runs in {BASELINES_DIR.relative_to(Path.cwd())}; "
            f"found {len(files)}. Capture more with the snippet in evals/README.md.",
            file=sys.stderr,
        )
        return 1

    sparse = [m for m, vals in obs.items() if len(vals) < MIN_OBSERVATIONS]
    if sparse:
        print(
            f"too few non-NaN observations for {sparse} (<{MIN_OBSERVATIONS}); refusing to derive degenerate floors.",
            file=sys.stderr,
        )
        return 1

    derived = _derive(obs)

    print(f"Read {len(files)} baseline run(s) from {BASELINES_DIR.relative_to(Path.cwd())}:")
    for path in files:
        print(f"  - {path.name}")
    print()
    print(f"{'metric':<22} {'n':>4} {'mean':>8} {'stdev':>8} {'floor':>8}")
    print("-" * 54)
    for metric, (n, mean, stdev, floor) in derived.items():
        print(f"{metric:<22} {n:>4d} {mean:>8.4f} {stdev:>8.4f} {floor:>8.4f}")
    print()

    floors = {metric: floor for metric, (_, _, _, floor) in derived.items()}
    THRESHOLDS_FILE.write_text(_render_thresholds_file(floors))
    print(f"Wrote {THRESHOLDS_FILE.relative_to(Path.cwd())}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
